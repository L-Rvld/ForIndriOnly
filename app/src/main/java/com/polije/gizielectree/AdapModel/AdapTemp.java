package com.polije.gizielectree.AdapModel;

public class AdapTemp {
    String hari, id;

    public AdapTemp(String hari, String id) {
        this.hari = hari;
        this.id = id;
    }

    public String getHari() {
        return hari;
    }

    public void setHari(String hari) {
        this.hari = hari;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
